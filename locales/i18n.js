// ============ i18n 工具函数 ============

export function getTranslation(language = 'en', path) {
  const lang = translations[language] || translations['en'];
  
  // 支持点符号路径 (例如 'payment.usdt')
  const keys = path.split('.');
  let value = lang;
  
  for (const key of keys) {
    value = value[key];
    if (!value) return path; // 如果找不到，返回原始路径
  }
  
  return value;
}

// ============ React Hook (用于前端) ============

// 在前端使用：
// import { useTranslation } from './i18n'
// const { t } = useTranslation('en');
// <h1>{t('account.balance')}</h1>

export function useTranslation(language = 'en') {
  return {
    t: (path) => getTranslation(language, path),
    language,
    availableLanguages: Object.keys(translations)
  };
}

// ============ Express 中间件 (用于后端) ============

export function i18nMiddleware(req, res, next) {
  const language = req.query.lang || req.headers['accept-language']?.split(',')[0] || 'en';
  req.i18n = {
    language: language.slice(0, 2),
    t: (path) => getTranslation(language.slice(0, 2), path)
  };
  next();
}

